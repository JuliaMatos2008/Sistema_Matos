    public JmfClientes viewBean(){
    JmfClientes jmfClientes = new JmfClientes();
    int codigo = Util.strToInt(jTxtCodigo.getText());
    jmfClientes.setJmfIdCliente(codigo);
    jmfClientes.setJmfNome(jTxtNome.getText());
    jmfClientes.setJmfCpf(jFmtCpf.getText());
    jmfClientes.setJmfRg(jFmtRg.getText());
    jmfClientes.setJmfSexo(jCboSexo.getSelectedItem().toString());
    jmfClientes.setJmfEmail(jTxtEmail.getText());
    jmfClientes.setJmfEndereco(jTxtEndereco.getText());
    jmfClientes.setJmfTelefone(jFmtTelefone.getText());
    jmfClientes.setJmfCelular(jFmtCelular.getText());
    jmfClientes.setJmfDataNascimento(Util.strDate(jFmtDatadeNascimento.getText()));
    jmfClientes.setJmfDataCadastro(Util.strDate(jFmtDataCadastro.getText()));
    jmfClientes.setJmfDataUltimaCompra(Util.strDate(jFmtDataUltimaCompra.getText()));
    if (jCboAtivo.isSelected()== true){
            jmfClientes.setJmfAtivo("S");
        } else {
            jmfClientes.setJmfAtivo("N");
        }

    return jmfClientes;
    }
    
    
     public void beanView(JmfClientes jmfClientes) {
    jTxtCodigo.setText(Util.intToStr(jmfClientes.getJmfIdCliente()));
    jTxtNome.setText(jmfClientes.getJmfNome());
    jFmtCpf.setText(jmfClientes.getJmfCpf());
    jFmtRg.setText(jmfClientes.getJmfRg());
    jTxtEndereco.setText(jmfClientes.getJmfEndereco());
    jFmtDatadeNascimento.setText(Util.dateToStr(jmfClientes.getJmfDataNascimento()));
    jFmtCelular.setText(jmfClientes.getJmfCelular());
    jFmtDataCadastro.setText(Util.dateToStr(jmfClientes.getJmfDataCadastro()));
    jFmtDataUltimaCompra.setText(Util.dateToStr(jmfClientes.getJmfDataUltimaCompra()));
    jFmtTelefone.setText(jmfClientes.getJmfTelefone());
    jCboSexo.setSelectedItem(jmfClientes.getJmfSexo());
    jTxtEmail.setText(jmfClientes.getJmfEmail());
     if (jmfClientes.getJmfAtivo().equals("S")== true){
        jCboAtivo.setSelected(true);
    } else {
        jCboAtivo.setSelected(false);
    }
    
    
}

