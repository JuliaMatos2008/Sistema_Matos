     public JmfVendedores viewBean(){
    JmfVendedores jmfVendedores = new JmfVendedores();
    
    
    int codigo = Util.strToInt(jTxtCodigoVendedor.getText());
    jmfVendedores.setJmfIdVendedor(codigo);
    jmfVendedores.setJmfNome(jTxtNomeVendedor.getText());
    jmfVendedores.setJmfApelido(jTxtApelidoVendedor.getText());
    jmfVendedores.setJmfCpf(jFmtCpfVendedor.toString());
    jmfVendedores.setJmfRg(jFmtrgVendedor.toString());
    jmfVendedores.setJmfEndereco(jTxtEnderecoVendedor.getText());
    jmfVendedores.setJmfCidade(jTxtCidadeVendedor.getText());
    jmfVendedores.setJmfEstado(jTxtEstadoVendedor.getText());
    jmfVendedores.setJmfEmail(jTxtEmailVendedor.getText());
    jmfVendedores.setJmfCelular(jFmtCelularVendedor.getText());
    jFmtDataNascimentoVendedor.setText(Util.dateToStr(jmfVendedores.getJmfDataNascimento()));
    if (jChbAtivoVendedor.isSelected()== true){
            jmfVendedores.setJmfAtivo("S")
                    ;
        } else {
            jmfVendedores.setJmfAtivo("N");
        }
  

    return jmfVendedores;
}
    public void beanView(JmfVendedores jmfVendedores) {
    jTxtCodigoVendedor.setText(Util.intToStr(jmfVendedores.getJmfIdVendedor()));
    jTxtNomeVendedor.setText(jmfVendedores.getJmfNome());
    jTxtApelidoVendedor.setText(jmfVendedores.getJmfApelido());
    jFmtCpfVendedor.setText(jmfVendedores.getJmfCpf());
    jFmtrgVendedor.setText(jmfVendedores.getJmfRg());
    jTxtEnderecoVendedor.setText(jmfVendedores.getJmfEndereco());
    jTxtCidadeVendedor.setText(jmfVendedores.getJmfCidade());
    jTxtEstadoVendedor.setText(jmfVendedores.getJmfEstado());
    jTxtEmailVendedor.setText(jmfVendedores.getJmfEmail());
    jFmtCelularVendedor.setText(jmfVendedores.getJmfCelular());
    jFmtDataNascimentoVendedor.setText(Util.dateToStr(jmfVendedores.getJmfDataNascimento()));

    if ("S".equals(jmfVendedores.getJmfAtivo())) {
        jChbAtivoVendedor.setSelected(true);
    } else {
        jChbAtivoVendedor.setSelected(false);
    }
}

